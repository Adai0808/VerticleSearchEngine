package zbf.search.model;

public class PaperModel {
	private String title = "";
	private String head = "";
	private String abstrct = "";
	private String content = "";
	private String conclu = "";
	private String refers = "";
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public String getAbstrct() {
		return abstrct;
	}
	public void setAbstrct(String abstrct) {
		this.abstrct = abstrct;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getConclu() {
		return conclu;
	}
	public void setConclu(String conclu) {
		this.conclu = conclu;
	}
	public String getRefers() {
		return refers;
	}
	public void setRefers(String refers) {
		this.refers = refers;
	}
	
	
}
